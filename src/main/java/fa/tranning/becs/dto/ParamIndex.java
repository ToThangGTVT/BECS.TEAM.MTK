package fa.tranning.becs.dto;

public class ParamIndex {

    private Integer pagePublisher;
    private Integer pageAuthor;
    private Integer pageTitle;
    private Integer pageAll;

    public Integer getPagePublisher() {
        return pagePublisher;
    }

    public void setPagePublisher(Integer pagePublisher) {
        this.pagePublisher = pagePublisher;
    }

    public Integer getPageAuthor() {
        return pageAuthor;
    }

    public void setPageAuthor(Integer pageAuthor) {
        this.pageAuthor = pageAuthor;
    }

    public Integer getPageTitle() {
        return pageTitle;
    }

    public void setPageTitle(Integer pageTitle) {
        this.pageTitle = pageTitle;
    }

    public Integer getPageAll() {
        return pageAll;
    }

    public void setPageAll(Integer pageAll) {
        this.pageAll = pageAll;
    }
}
